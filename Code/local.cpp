{

	ImGui::SetScrollX(0);
	ImGui::GetStyle().WindowPadding = ImVec2(11, 11);
	g_UiManager->Toggle("Godmode",false, &toggles::self::godmode);
	g_UiManager->Toggle("Never Wanted", false, &toggles::self::never_wanted);
	g_UiManager->Toggle("Superman", false, &toggles::self::superman);
	g_UiManager->Toggle("Ragdoll", false, &toggles::self::ragdoll);
	g_UiManager->Toggle("Off Radar", false, &toggles::self::off_radar);
	g_UiManager->Toggle("Invisibility", false, &toggles::self::invisible, [] {
		self::invisible(toggles::self::invisible);
		});

	g_UiManager->Button("Spawn Notification", true, []
		{
			g_UiManager->Notification({ ImGuiNotificationType_Success, 3000, "Chimera has prevented a crash"});
			g_UiManager->Notification({ ImGuiNotificationType_Warning, 3000, "Chimera has loaded all data"});
			g_UiManager->Notification({ ImGuiNotificationType_Error, 3000, "Chimera has successfully loaded"});
			g_UiManager->Notification({ ImGuiNotificationType_Info, 3000,  "Chimera failed to initialize"});
			g_UiManager->Notification({ ImGuiNotificationType_None, 3000,  "Chimera has experienced an error"});
			ImGui::InsertNotification({ ImGuiNotificationType_Success, 3000, "Chimera Successfully Loaded" });
		});
	g_UiManager->Button("Spawn Adder",false, [] {
		Hash hash = MISC::GET_HASH_KEY("adder");
		LOG(INFO) << hash;
		STREAMING::REQUEST_MODEL(hash);
		while (!STREAMING::HAS_MODEL_LOADED(hash))
		{
			STREAMING::REQUEST_MODEL(hash);
			script::get_current()->yield(0ms);
		}
		Ped iPed = PLAYER::PLAYER_PED_ID();

		NativeVector3 location = ENTITY::GET_ENTITY_COORDS(iPed, true);
		LOG(INFO) << "X: " << location.x << " Y: " << location.y << " Z: " << location.z;
		auto veh = VEHICLE::CREATE_VEHICLE(hash, location.x, location.y, location.z, 0, *g_Hooking->m_is_session_started, false, false);
		STREAMING::SET_MODEL_AS_NO_LONGER_NEEDED(hash);

		});
}